bool process_record_user(uint16_t keycode, keyrecord_t *record) {

    mod_state = get_mods();

    switch (keycode) {
    
        case LOWER:
            if (record->event.pressed) {
                layer_on(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _TUNE);
            } else {
                layer_off(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _TUNE);
            }
            return false;

        case RAISE:
            if (record->event.pressed) {
                layer_on(_RAISE);
                update_tri_layer(_LOWER, _RAISE, _TUNE);
            } else {
                layer_off(_RAISE);
                update_tri_layer(_LOWER, _RAISE, _TUNE);
            }
            return false;         
                 
        // case QUOTE:
        //     if (record->event.pressed) {
        //         register_code(KC_QUOTE);
        //         register_code(KC_SPACE);
        //     } else { 
        //         unregister_code(KC_QUOTE);   
        //         unregister_code(KC_SPACE);
        //     }
        //     return false;       

        case HUI:
            if (record->event.pressed) {
                current_hue = current_hue + 5;
                if (current_hue > 255) {
                    current_hue = current_hue - 256;
                }    
            } else {
                ;
            }
            return false;    
            
        case HUD:
            if (record->event.pressed) {
                current_hue = current_hue - 5;
                if (current_hue < 0) {
                    current_hue = 256 + current_hue;
                }    
            } else {
                ;
            }
            return false;
            
        case RALT(KC_SLSH):
            if (record->event.pressed) { // al presionar el keycode
                if (mod_state & MOD_MASK_SHIFT) { // si SHIFT esta presionado
                    del_mods(MOD_MASK_SHIFT); // desactivar SHIFT
                    register_code16(RALT(KC_1)); // ya que RALT(KC_1) entrega el simbolo �
                    key_registered = true; 
                    set_mods(mod_state); // volver a activar SHIFT
                    return false; // ya que true registraria ademas el keycode RALT(KC_SLSH) simbolo �
                }
            } else { // al liberar el keycode
                if (key_registered) {
                    unregister_code16(RALT(KC_1)); // dejar de registrar RALT(KC_1)
                    key_registered = false;
                    return false;
                }
            }
            return true; // lo anterior es solo para cuando esta presionado SHIFT. Si no lo esta, simplemente hacer RALT(KC_SLSH)
        
        case KC_PPLS:
            if (record->event.pressed) { // al presionar el keycode
                if (mod_state & MOD_MASK_SHIFT) { // si SHIFT esta presionado
                    del_mods(MOD_MASK_SHIFT); // desactivar SHIFT
                    register_code16(KC_EQUAL); // ya que KC_EQUAL entrega el simbolo =
                    key_registered = true; 
                    set_mods(mod_state); // volver a activar SHIFT
                    return false; // ya que true registraria ademas el keycode KC_PPLS simbolo +
                }
            } else { // al liberar el keycode
                if (key_registered) {
                    unregister_code16(KC_EQUAL); // dejar de registrar KC_EQUAL
                    key_registered = false;
                    return false;
                }
            }
            return true; // lo anterior es solo para cuando esta presionado SHIFT. Si no lo esta, simplemente hacer KC_PPLS
    }
    return true;
}